"""
Використовуючи підпрограму для знаходження найбільшого спільного дільника (НСД), знайти значення виразу
S=(НСД(a,b)+ НСД(a,4))+ НСД(24,b)
"""
#0
"""
NSD - int - Найбільший Спільний Дільник
a, b - int - числа, які вводяться з клавіатури
"""

def NSD(x,y):
    while y != 0:
            (x, y) = (y, x % y)
    return x
a = int(input("Перше число: "))
b = int(input("Друге число: "))
print(int(NSD(a,b) + NSD(a,4)) + NSD(24, b))